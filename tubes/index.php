            <br>
            Kemudian pada tahun 1971, editor in chief Vogue kala itu, Diana Vreeland, memiliki ide untuk mengubah acara tersebut menjadi pesta yang menerapkan tema tahunan. Sejak itu, pihaknya mengirim undangan ke selebritas, lalu perlahan Met Gala menjadi salah satu malam terpenting di kalender sosial New York, AS.</p>
            <br>
            <p>The Met menyimpan koleksi lebih dari 33.000 kostum yang berasal dari lima benua dan tujuh abad pakaian modis. Tak hanya itu, museum tersebut juga menyimpan aksesori untuk pria, wanita, dan anak-anak, dari abad ke-15 hingga kini.
