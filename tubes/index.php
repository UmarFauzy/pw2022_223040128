            <p>Met Gala merupakan acara tahunan berkonsep night fashion yang bertujuan untuk amal. Met Gala diselenggarakan oleh pemimpin redaksi Vogue, Anna Wintour untuk penggalangan dana Metropolitan Museum of Art</p>
